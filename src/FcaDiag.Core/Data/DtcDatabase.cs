    /// <summary>
    /// Gets the description for a DTC code
    /// </summary>
    public static DtcInfo? GetInfo(string dtcCode)
    {
        return _dtcDescriptions.TryGetValue(dtcCode.ToUpperInvariant(), out var info) ? info : null;
    }

    /// <summary>
    /// Gets the description for a DTC code, or a default message if not found
    /// </summary>
    public static string GetDescription(string dtcCode)
    {
        var info = GetInfo(dtcCode);
        return info?.Description ?? "No description available";
    }

    /// <summary>
    /// Gets the category for a DTC code
    /// </summary>
    public static string GetCategory(string dtcCode)
    {
        var info = GetInfo(dtcCode);
        return info?.Category ?? "Unknown";
    }
}

